void process_layer_state(void) {
    switch (get_highest_layer(layer_state)) {
        case 0:
            render_layer1_logo();
            break;
        case 1:
            render_layer2_logo();
            break;
        case 2:
            render_layer3_logo();
            break;
        case 3:
            render_layer4_logo();
            break;
        case 4:
            render_layer5_logo();
            break;
        case 5:
            render_layer6_logo();
            break;
    }
}

bool oled_task_kb(void) {
    if (!oled_task_user()) { return false; }
    if (is_keyboard_master()) {
        process_layer_state();
    } else {
        if (IS_HOST_LED_ON(USB_LED_CAPS_LOCK) && IS_HOST_LED_ON(USB_LED_SCROLL_LOCK)) {
            render_caps_lock_scroll_lock_logo();
        }
        else if (IS_HOST_LED_ON(USB_LED_CAPS_LOCK) && !IS_HOST_LED_ON(USB_LED_SCROLL_LOCK)) {
            render_caps_lock_logo();
        }
        else if (!IS_HOST_LED_ON(USB_LED_CAPS_LOCK) && IS_HOST_LED_ON(USB_LED_SCROLL_LOCK)) {
            render_scroll_lock_logo();
        }
        else {
            render_lulu_logo();
        }
    }
    return false;
}
